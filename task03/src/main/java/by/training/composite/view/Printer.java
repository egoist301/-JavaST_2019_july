package by.training.composite.view;

/**
 * Printer.
 */
public final class Printer {
    /**
     * Default constructor.
     */
    private Printer() {
    }

    /**
     * Print info on console.
     *
     * @param message message to print.
     */
    public static void printInfo(final Object message) {
        System.out.println(message);
    }
}
